use proconio::{input, marker::Chars};

fn main() {
    input! {
        n: usize,
        m: usize,
        s: [Chars; n]
    };
    let tak = [
        ['#', '#', '#', '.', '?', '?', '?', '?', '?'],
        ['#', '#', '#', '.', '?', '?', '?', '?', '?'],
        ['#', '#', '#', '.', '?', '?', '?', '?', '?'],
        ['.', '.', '.', '.', '?', '?', '?', '?', '?'],
        ['?', '?', '?', '?', '?', '?', '?', '?', '?'],
        ['?', '?', '?', '?', '?', '.', '.', '.', '.'],
        ['?', '?', '?', '?', '?', '.', '#', '#', '#'],
        ['?', '?', '?', '?', '?', '.', '#', '#', '#'],
        ['?', '?', '?', '?', '?', '.', '#', '#', '#'],
    ];

    for i in 0..=n - 9 {
        for j in 0..=m - 9 {
            let mut ok = true;
            for ti in 0..9 {
                for tj in 0..9 {
                    if tak[ti][tj] == '?' {
                        continue;
                    }
                    if s[i + ti][j + tj] != tak[ti][tj] {
                        ok = false;
                    }
                }
            }
            if ok {
                println!("{} {}", i + 1, j + 1);
            }
        }
    }
}
