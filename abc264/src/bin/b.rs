use itertools::Itertools;
use proconio::input;

fn main() {
    input! {
        r: usize,
        c: usize,
    };

    let x = [
        "###############",
        "#.............#",
        "#.###########.#",
        "#.#.........#.#",
        "#.#.#######.#.#",
        "#.#.#.....#.#.#",
        "#.#.#.###.#.#.#",
        "#.#.#.#.#.#.#.#",
        "#.#.#.###.#.#.#",
        "#.#.#.....#.#.#",
        "#.#.#######.#.#",
        "#.#.........#.#",
        "#.###########.#",
        "#.............#",
        "###############",
    ];

    if x[r - 1].chars().into_iter().collect_vec()[c - 1] == '#' {
        println!("black");
    } else {
        println!("white");
    }
}
